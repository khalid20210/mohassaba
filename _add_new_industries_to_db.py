def get_db():
    con = sqlite3.connect(DB)
    con.row_factory = sqlite3.Row
    return con


def get_existing_industry_types():
    con = get_db()
    rows = con.execute("SELECT DISTINCT industry_type FROM businesses WHERE is_active=1").fetchall()
    con.close()
    return {r[0] for r in rows}


def get_all_industry_types_from_config():
    return [(k, v) for k, v in INDUSTRY_TYPES]


def add_business_and_products(con, industry_type, industry_name, business_id, products):
    # إضافة المنشأة
    con.execute("""
        INSERT INTO businesses (name, industry_type, is_active, created_at)
        VALUES (?, ?, 1, datetime('now'))
    """, (f"منشأة {industry_name}", industry_type))
    bid = con.execute("SELECT last_insert_rowid()").fetchone()[0]

    # إضافة المنتجات
    for i, (name, cat, price, cost) in enumerate(products):
        sku = f"{industry_type[:8].upper()}-{business_id:04d}-{i+1:03d}"
        con.execute("""
            INSERT INTO products (business_id, name, name_en, barcode, category_name,
                                  sale_price, purchase_price, is_active, track_stock, can_sell,
                                  can_purchase, is_pos, created_at)
            VALUES (?, ?, '', ?, ?, ?, ?, 1, 1, 1, 1, 1, datetime('now'))
        """, (bid, name, sku, cat, price, cost))
        prod_id = con.execute("SELECT last_insert_rowid()").fetchone()[0]

        # إضافة مخزون إذا كان الجدول موجوداً
        try:
            con.execute("""
                INSERT OR IGNORE INTO inventory (product_id, business_id, quantity, min_quantity, updated_at)
                VALUES (?, ?, ?, 5, datetime('now'))
            """, (prod_id, bid, random.randint(20, 200)))
        except Exception:
            pass

    return bid


