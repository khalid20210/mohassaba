def _make_barcode(prod_id: int, biz_id: int) -> str:
    h = hashlib.md5(f"{biz_id}-{prod_id}".encode()).hexdigest()
    digits = ''.join(c for c in h if c.isdigit())[:12]
    return digits.ljust(12, '0')[:12]


