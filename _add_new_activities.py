def _barcode():
    digits = [random.randint(0,9) for _ in range(12)]
    total = sum(d * (1 if i%2==0 else 3) for i,d in enumerate(digits))
    check = (10 - (total%10)) % 10
    return "".join(str(d) for d in digits) + str(check)

def _gen_products(catalog: dict, target: int) -> list[dict]:
    """يولد قائمة منتجات بحيث يتم تكرار الأسماء بأرقام تسلسلية لبلوغ الهدف."""
    base_items = []
    for cat, names in catalog.items():
        for n in names:
            base_items.append({"cat": cat, "name": n})
    
    products = []
    serial = 0
    cycle = 0
    while len(products) < target:
        for item in base_items:
            serial += 1
            cycle_suffix = f" {cycle+1}" if cycle > 0 else ""
            products.append({
                "cat": item["cat"],
                "name": f"{item['name']}{cycle_suffix}",
                "serial": serial,
            })
            if len(products) >= target:
                break
        cycle += 1
    return products[:target]

# ─── إنشاء نشاط تجاري ────────────────────────────────────────────────────────

def create_business(cur, name_ar, name_en, industry_type, city) -> int:
    """يُنشئ business جديد ويعيد الـ id."""
    cur.execute("""
        INSERT INTO businesses (name, name_en, industry_type, city, country, currency, country_code, is_active)
        VALUES (?, ?, ?, ?, 'SA', 'SAR', 'SA', 1)
    """, (name_ar, name_en, industry_type, city))
    return cur.lastrowid

# ─── إنشاء فئات المنتجات لنشاط ──────────────────────────────────────────────

def create_categories(cur, biz_id: int, catalog: dict) -> dict:
    """يُنشئ تصنيفات ويعيد {cat_name: cat_id}."""
    cat_ids = {}
    for cat_name in catalog.keys():
        cur.execute("""
            INSERT OR IGNORE INTO product_categories (business_id, name, is_active)
            VALUES (?, ?, 1)
        """, (biz_id, cat_name))
        cur.execute("""
            SELECT id FROM product_categories WHERE business_id=? AND name=?
        """, (biz_id, cat_name))
        row = cur.fetchone()
        cat_ids[cat_name] = row[0]
    return cat_ids

# ─── إدراج المنتجات دفعة واحدة ───────────────────────────────────────────────

def insert_products(cur, biz_id: int, prefix: str, catalog: dict, target: int) -> int:
    products = _gen_products(catalog, target)
    cat_ids = create_categories(cur, biz_id, catalog)
    
    # أعلى رقم SKU حالي لهذا الـ biz
    cur.execute("""
        SELECT MAX(CAST(REPLACE(REPLACE(sku, ?||'-', ''), '-', '') AS INTEGER))
        FROM product_inventory WHERE business_id=?
    """, (prefix, biz_id))
    row = cur.fetchone()
    start_seq = (row[0] or 0) + 1
    
    prod_rows = []
    inv_rows  = []
    for i, p in enumerate(products):
        cat_id = cat_ids.get(p["cat"])
        bc = _barcode()
        seq = start_seq + i
        sku = f"{prefix}-{seq:04d}"
        purchase = round(random.uniform(20, 200), 2)
        sale     = round(purchase * random.uniform(1.1, 1.4), 2)
        
        prod_rows.append((
            biz_id, bc, p["name"], "product",
            cat_id, p["cat"],
            1, purchase, 1, sale,
            0, 1, 1, 1,
        ))
        inv_rows.append((biz_id, sku, bc, round(random.uniform(10,200)), purchase, sale))
    
    cur.executemany("""
        INSERT INTO products
          (business_id, barcode, name, product_type,
           category_id, category_name,
           can_purchase, purchase_price, can_sell, sale_price,
           min_stock, track_stock, is_pos, is_active)
        VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?)
    """, prod_rows)
    
    # نحتاج الـ IDs الجديدة
    first_id = cur.lastrowid - len(prod_rows) + 1
    inv_rows_final = []
    for j, ir in enumerate(inv_rows):
        biz, sku, bc, qty, cost, price = ir
        inv_rows_final.append((biz, first_id+j, sku, bc, qty, cost, price))
    
    cur.executemany("""
        INSERT INTO product_inventory
          (business_id, product_id, sku, barcode, current_qty, unit_cost, unit_price)
        VALUES (?,?,?,?,?,?,?)
    """, inv_rows_final)
    
    return len(products)

