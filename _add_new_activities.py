def main():
    conn = sqlite3.connect(DB)
    cur  = conn.cursor()
    
    total_added = 0
    for name_ar, name_en, industry_type, prefix, city in ACTIVITIES:
        catalog = CATALOG_MAP[industry_type]
        
        # تحقق إن النشاط موجود مسبقاً
        cur.execute("SELECT id FROM businesses WHERE name=?", (name_ar,))
        row = cur.fetchone()
        if row:
            biz_id = row[0]
            print(f"  ↩ موجود مسبقاً: {name_ar} [id={biz_id}]")
        else:
            biz_id = create_business(cur, name_ar, name_en, industry_type, city)
            print(f"  ✅ أُنشئ: {name_ar} [id={biz_id}]")
        
        # عدد المنتجات الحالية
        cur.execute("SELECT COUNT(*) FROM products WHERE business_id=?", (biz_id,))
        current = cur.fetchone()[0]
        
        if current >= TARGET:
            print(f"     ↩ يمتلك {current:,} منتج — لا حاجة لإضافة")
            continue
        
        need = TARGET - current
        added = insert_products(cur, biz_id, prefix, catalog, need)
        total_added += added
        
        cur.execute("SELECT COUNT(*) FROM products WHERE business_id=?", (biz_id,))
        final = cur.fetchone()[0]
        print(f"     ✅ أُضيف {added:,} منتج → الإجمالي الآن: {final:,}")
    
    conn.commit()
    conn.close()
    print(f"\n{'─'*50}")
    print(f"✅ انتهى — إجمالي المضاف: {total_added:,} منتج")
    print(f"{'─'*50}")

if __name__ == "__main__":
    main()
