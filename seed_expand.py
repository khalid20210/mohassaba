def insert_products(cur, biz_id, products, cat_map):
    added = 0
    for p in products:
        name, name_en, barcode, cat_name, sale_price, purchase_price = p
        if cur.execute("SELECT 1 FROM products WHERE business_id=? AND name=?", (biz_id, name)).fetchone():
            continue
        pt = "service" if purchase_price == 0 else "product"
        cur.execute("""
            INSERT INTO products (business_id, barcode, name, name_en, product_type,
               category_id, category_name, can_purchase, purchase_price, can_sell, sale_price,
               track_stock, is_pos, is_active, created_at, updated_at)
            VALUES (?,?,?,?,?, ?,?, 1,?,1,?, ?,1,1, ?,?)
        """, (biz_id, barcode, name, name_en, pt,
              cat_map.get(cat_name), cat_name,
              purchase_price, sale_price,
              0 if pt == "service" else 1, NOW, NOW))
        added += 1
    return added

def ensure_cats(cur, biz_id, cats):
    cat_map = {}
    for c in cats:
        row = cur.execute("SELECT id FROM product_categories WHERE business_id=? AND name=?", (biz_id, c)).fetchone()
        if row:
            cat_map[c] = row[0]
        else:
            cur.execute("INSERT INTO product_categories (business_id, name, is_active) VALUES (?,?,1)", (biz_id, c))
            cat_map[c] = cur.lastrowid
    return cat_map

def get_biz_id(cur, name):
    row = cur.execute("SELECT id FROM businesses WHERE name=?", (name,)).fetchone()
    return row[0] if row else None

def create_biz(cur, name, name_en, industry_type, city):
    row = cur.execute("SELECT id FROM businesses WHERE name=?", (name,)).fetchone()
    if row:
        print(f"  ⚠️  موجودة: {name} (id={row[0]})")
        return row[0]
    cur.execute("""INSERT INTO businesses (name,name_en,industry_type,city,is_active,created_at,updated_at)
                   VALUES (?,?,?,?,1,?,?)""", (name, name_en, industry_type, city, NOW, NOW))
    bid = cur.lastrowid
    print(f"  ✅ جديدة: {name} ({industry_type}) id={bid}")
    return bid

