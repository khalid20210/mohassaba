def seed():
    conn = sqlite3.connect(DB)
    conn.row_factory = sqlite3.Row
    cur = conn.cursor()
    added_b = added_p = added_c = 0

    # ── توسيع المنشآت الموجودة ────────────────────────────────────────
    EXPANSIONS = [
        ("مطعم الوجبات الذهبية",   REST_EXTRA_CATS,    REST_EXTRA),
        ("صيدلية الشفاء",          PHARMA_EXTRA_CATS,  PHARMA_EXTRA),
        ("ملحمة الخير الطازجة",    BUTCHER_EXTRA_CATS, BUTCHER_EXTRA),
        ("كافيه السحاب",           CAFE_EXTRA_CATS,    CAFE_EXTRA),
        ("بوتيك الرجل الأنيق",     FASH_EXTRA_CATS,    FASH_EXTRA),
        ("ورشة الأمين للسيارات",   WORK_EXTRA_CATS,    WORK_EXTRA),
        ("بقالة الأمل",            GROCERY_EXTRA_CATS, GROCERY_EXTRA),
    ]
    for biz_name, cats, products in EXPANSIONS:
        bid = get_biz_id(cur, biz_name)
        if not bid:
            print(f"  ⚠️  لم تُوجد: {biz_name}")
            continue
        cat_map = ensure_cats(cur, bid, cats)
        # أيضاً أعِد جلب الفئات الموجودة مسبقاً (للفئات المشتركة)
        all_cats = cur.execute("SELECT id, name FROM product_categories WHERE business_id=?", (bid,)).fetchall()
        for row in all_cats:
            cat_map[row["name"]] = row["id"]
        n = insert_products(cur, bid, products, cat_map)
        added_p += n
        print(f"  ➕ {biz_name}: أضيف {n} منتج")

    # ── إنشاء المنشآت الجديدة ─────────────────────────────────────────
    print()
    for biz in NEW_BUSINESSES:
        bid = create_biz(cur, biz["name"], biz.get("name_en",""),
                         biz["industry_type"], biz.get("city",""))
        cat_map = ensure_cats(cur, bid, biz["categories"])
        n = insert_products(cur, bid, biz["products"], cat_map)
        added_p += n
        added_b += 1
        added_c += len(biz["categories"])
        print(f"     ↳ أضيف {n} منتج")

    conn.commit()
    conn.close()
    print()
    print("═" * 55)
    print(f"✅ انتهى — منشآت جديدة: {added_b} | منتجات مضافة: {added_p}")

if __name__ == "__main__":
    print("جاري التوسيع والإضافة...")
    print("═" * 55)
    seed()
